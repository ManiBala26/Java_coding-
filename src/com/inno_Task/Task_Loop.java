package com.inno_Task;

public class Task_Loop {
	public static void main(String[] args) {
		for (int i=2; i <=5; i++) {
			for (int j =2; j <=i ; j++) {
				System.out.print( j +" " );
				
			}
			System.out.println( );
		}
		System.out.println( );
		for (int i = 3; i <=6; i++) {
			for (int j = 1; j <=i; j++) {
				System.out.print( "* ");
			}
			System.out.println();
		}
		System.out.println();
		for (int i = 3; i <=7; i++) {
		for (int j = 1; j <=i; j++) {
			System.out.print("$ ");
		}
		System.out.println();
		}
		
	}
  
}
	
	
	
	

